package bean_proyecto;
public class bean_tesistas {
    
    private int id_alumtes;
    private String estado;
    private int id_alumno;
    private int id_tesis;

    public int getId_alumtes() {
        return id_alumtes;
    }

    public void setId_alumtes(int id_alumtes) {
        this.id_alumtes = id_alumtes;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public int getId_alumno() {
        return id_alumno;
    }

    public void setId_alumno(int id_alumno) {
        this.id_alumno = id_alumno;
    }

    public int getId_tesis() {
        return id_tesis;
    }

    public void setId_tesis(int id_tesis) {
        this.id_tesis = id_tesis;
    }
    
    
    
    
}
